package com.labymedia.ultralight.databind.api;

import com.labymedia.ultralight.databind.Databind;
import com.labymedia.ultralight.javascript.JavascriptContext;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Marks the method as accepting a {@link JavascriptContext} as the <b>first</b>
 * parameter.
 * <p>
 * The {@link Databind} instance will then a call the method with the current context.
 */
@Target(ElementType.METHOD)
@Retention(RetentionPolicy.RUNTIME)
public @interface InjectJavascriptContext {
}
